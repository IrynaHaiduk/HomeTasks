package de.telran.lesson3;

public class Calc {

    public double add(double x, double y) {
        return x + y;
    }
    public double sub(double x, double y) {
        return x - y;
    }
    public double mult(double x, double y) {
        return x * y;
    }
    public double div(double x, double y) {
        return x / y;
    }
}
